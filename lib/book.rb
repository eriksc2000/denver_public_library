class Book
  attr_reader :book, 
              :author_first_name,
              :author_last_name,
              :title,
              :publication_date
              
  
  def initialize
    @book = book 
    @author_first_name = "Harper"
    @author_last_name = "Lee"
    @title = "To Kill a Mockingbird"
    @publication_date = 1960
  end
 
    

end
